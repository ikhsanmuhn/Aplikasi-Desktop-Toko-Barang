    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        tabmodel = (DefaultTableModel) tabel_pegawai.getModel();
        int a = JOptionPane.showConfirmDialog(null, "yakin ingin menghapus data "
                    +namaKaryawan+" ?", "Warning", JOptionPane.YES_NO_OPTION);
            if (a == JOptionPane.YES_OPTION) {
                try {
                    
                    k.query = "delete from login where Kode_Karyawan ='"+kodeKaryawan+"'";
                    k.setData();
                    k.ps.executeUpdate();
                    
                    
                    k.query = "delete from karyawan where Kode_Karyawan ='"+kodeKaryawan+"'";
                    k.setData();
                    k.ps.executeUpdate();
                    
                    
                    showdata();
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus","info",JOptionPane.INFORMATION_MESSAGE);
                } catch (SQLException ex) {
                    System.out.println("error");
                }
                
            } 
    }//GEN-LAST:event_jButton4MouseClicked
    
    
    
    void showdata(){
        tabmodel = (DefaultTableModel) tabel_pegawai.getModel();
        tabmodel.getDataVector().removeAllElements();
        revalidate();
        k.query = "select Kode_Karyawan, Nama_Karyawan, Alamat, JK, No_HP from karyawan"; 
        k.getData();
        try {
            while(k.rs.next()){
                //menampilkan database ke frame
                z = k.rs.getString("Kode_Karyawan");
                d = k.rs.getString("Nama_Karyawan");
                c = k.rs.getString("Alamat");
                v = k.rs.getString("JK");
                b = k.rs.getString("No_HP");
                String[] data = {z,d,c,v,b};
                tabmodel.addRow(data);
            }   
        } catch (SQLException ex) {
            System.out.print(ex);
        }
    }


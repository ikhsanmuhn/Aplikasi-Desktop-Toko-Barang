    private void cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariActionPerformed
        try {
            tabmodel = (DefaultTableModel) tabel_pegawai.getModel();
            tabmodel.getDataVector().removeAllElements();
            revalidate();
            search = cari.getText();
            
            k.query = "select Kode_Karyawan, Nama_Karyawan, Alamat, JK, No_HP from karyawan where Nama_Karyawan like '%"+search+"%' or Kode_Karyawan LIKE '%"
                    +search+"%' or Alamat like '%"+search+"%' or JK like '%"+search+"%' or No_HP like '%"
                    +search+"%'"; 
           
            k.getData();
            while(k.rs.next()){
                //menampilkan database ke frame
                z = k.rs.getString("Kode_Karyawan");
                d = k.rs.getString("Nama_Karyawan");
                c = k.rs.getString("Alamat");
                v = k.rs.getString("JK");
                b = k.rs.getString("No_HP");
                String[] data = {z,d,c,v,b};
                tabmodel.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Pelanggan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cariActionPerformed

